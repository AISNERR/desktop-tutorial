int	ft_count_if(char **tab, int length, int(*f)(char*))
{
	int	i;
	int	nb;

	i = 0;
	nb = 0;
	while (i < length)
	{
		if (f(tab[i]) != 0)
			nb++;
		i++;
	}
	return (nb);
}
