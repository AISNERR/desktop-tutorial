char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	h;

	i = 0;
	while (dest[i] != '\0')
		i++;
	h = 0;
	while (src[h] != '\0')
	{
		dest[i + h] = src[h];
		h++;
	}
	dest[i + h] = '\0';
	return (dest);
}
